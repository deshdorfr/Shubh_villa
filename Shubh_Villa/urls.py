from django.contrib import admin
from django.urls import path, include, re_path
from django.views.generic import TemplateView
from rest_framework import permissions
from drf_yasg.views import get_schema_view
from drf_yasg import openapi


schema_view = get_schema_view(
    openapi.Info(
        title="Shubh Villa Society API",
        default_version='v1',
        description="API for managing residents, maintenance, and more.",
        contact=openapi.Contact(email="admin@shubhvilla.com"),
        license=openapi.License(name="MIT License"),
    ),
    public=True,
    permission_classes=(permissions.AllowAny,),
)

urlpatterns = [
    path('admin/', admin.site.urls),
    path('api/residents/', include('residents.urls')),
    path('api/maintenance/', include('maintenance.urls')),
    # path('events/', include('events.urls')),
    # path('complaints/', include('complaints.urls')),
    
    # ðŸ”¥ Swagger URLs
    path('swagger/', schema_view.with_ui('swagger', cache_timeout=0), name='schema-swagger-ui'),
    path('redoc/', schema_view.with_ui('redoc', cache_timeout=0), name='schema-redoc'),
    re_path(r'^.*$', TemplateView.as_view(template_name='index.html')),  # React entrypoint
]
